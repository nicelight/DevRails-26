function pickerStartDirectory() {
  const cwd = resolve(process.cwd());
  return cwd === repoRoot ? dirname(cwd) : cwd;
}

function readDirectoryChoices(openDir) {
  try {
    const dirs = readdirSync(openDir, { withFileTypes: true })
      .filter((entry) => entry.isDirectory())
      .map((entry) => entry.name)
      .sort((a, b) => a.localeCompare(b, 'ru'));

    return {
      dirs: dirs.slice(0, MAX_PICKER_ENTRIES),
      hiddenCount: Math.max(0, dirs.length - MAX_PICKER_ENTRIES),
      error: null,
    };
  } catch (error) {
    return { dirs: [], hiddenCount: 0, error };
  }
}

function printFolderPicker(openDir, choices) {
  uiCardStart('Выберите папку проекта');
  uiCardLine(uiTone('Текущая папка', 'muted'));
  uiCardLine(uiTone(openDir, 'silver', true));
  uiCardLine();

  if (choices.error) {
    uiCardLine(uiTone('Не удалось прочитать список папок.', 'danger'));
  } else if (choices.dirs.length === 0) {
    uiCardLine(uiTone('Внутри нет папок.', 'muted'));
  } else {
    choices.dirs.forEach((name, index) => {
      uiCardLine(`${uiKeycap(String(index + 1).padStart(2, '0'))}  ${uiTone(`${name}/`, 'silver')}`);
    });
    if (choices.hiddenCount > 0) {
      uiCardLine(uiTone(`… ещё ${choices.hiddenCount}`, 'muted'));
    }
  }

  uiCardLine();
  uiCardLine(`${uiKeycap('№')} открыть   ${uiKeycap('U / ..')} вверх   ${uiKeycap('S')} выбрать`);
  uiCardLine(`${uiKeycap('P')} путь      ${uiKeycap('N')} новая    ${uiKeycap('Q')} отмена`);
  uiCardEnd();
}

function resolvePickerInput(openDir, input, choices) {
  const number = Number(input);
  if (!Number.isInteger(number) || number < 1 || number > choices.dirs.length) return null;
  return join(openDir, choices.dirs[number - 1]);
}

function printInvalidPickerNumber(input, choices) {
  if (choices.dirs.length === 0) {
    printUiNotice('Список пуст. Используйте U/.., S, P, N или Q.');
    return;
  }

  printUiNotice(`Нет пункта ${input}. Доступны пункты 1..${choices.dirs.length}.`);
}

async function createFolderInPicker(rl, openDir) {
  const name = (await rl.question(uiPrompt('Имя новой папки'))).trim();
  if (!name) {
    printUiNotice('Имя не задано.');
    return openDir;
  }

  if (name === '.' || name === '..' || name.includes('/') || name.includes('\\')) {
    printUiNotice('Введите только имя папки без пути.');
    return openDir;
  }

  const newDir = join(openDir, name);
  if (existsSync(newDir) && !statSync(newDir).isDirectory()) {
    printUiNotice('Такой путь уже существует, но это не папка.');
    return openDir;
  }

  mkdirSync(newDir, { recursive: true });
  printUiNotice(`Создана папка: ${newDir}`, 'success');
  return newDir;
}

async function pickTargetDirectory(rl) {
  let openDir = pickerStartDirectory();

  while (true) {
    const choices = readDirectoryChoices(openDir);
    printFolderPicker(openDir, choices);

    const input = (await rl.question(uiPrompt('Ваш выбор'))).trim();
    const normalized = input.toLowerCase();

    if (normalized === 'q') {
      printUiNotice('Установка отменена.', 'danger');
      process.exit(1);
    }

    if (normalized === '..' || normalized === 'u') {
      openDir = dirname(openDir);
      continue;
    }

    if (normalized === 's') return openDir;

    if (normalized === 'p') {
      const manualPath = (await rl.question(uiPrompt('Путь к проекту'))).trim();
      if (!manualPath) {
        printUiNotice('Путь не задан.');
        continue;
      }
      return resolve(manualPath);
    }

    if (normalized === 'n') {
      openDir = await createFolderInPicker(rl, openDir);
      continue;
    }

    const selectedDir = resolvePickerInput(openDir, normalized, choices);
    if (selectedDir) {
      openDir = selectedDir;
      continue;
    }

    if (/^\d+$/.test(normalized)) {
      printInvalidPickerNumber(normalized, choices);
      continue;
    }

    printUiNotice('Не понял выбор. Введите номер или клавишу из панели.');
  }
}

async function interactiveInstall() {
  if (!process.stdin.isTTY || !process.stdout.isTTY) {
    console.error('Interactive installer requires a TTY. Use --bootstrap --target <path> --yes for non-interactive setup.');
    process.exit(1);
  }

  console.log(colorizeSplashArt(SPLASH_ART));
  console.log(`\n${colorizeUiGradient(`SETUP  ·  ${PRODUCT_NAME}`)}`);
  console.log(uiTone('Выберите проект, проверьте настройки и запустите установку.', 'muted'));

  const rl = createInterface({ input: process.stdin, output: process.stdout });

  try {
    const targetRepo = await pickTargetDirectory(rl);
    const inspection = inspectTarget(targetRepo);

    if (inspection.exists && !inspection.isDirectory) {
      console.error(`Выбранный путь существует, но это не папка: ${targetRepo}`);
      process.exit(1);
    }

    printTargetInspection(targetRepo, inspection, true);

    if (!inspection.writable) {
      printUiNotice('Нет доступа для записи. Выберите другую папку и запустите установку снова.', 'danger');
      process.exit(1);
    }

    const warnings = [];
    if (!inspection.exists) warnings.push('Папка будет создана.');
    if (!inspection.git.isRepo && inspection.exists) warnings.push('Git-репозиторий не найден. Это не блокирует установку.');
    if (inspection.git.clean === false) warnings.push('Есть несохраненные git-изменения. Установка продолжится только после подтверждения.');
    if (inspection.agentsExists) warnings.push('AGENTS.md уже есть. Нужно выбрать замену или слияние.');
    if (inspection.memoryBankExists) {
      warnings.push('.memory-bank/ уже есть. Будет выполнено обновление существующей установки.');
    }

    if (warnings.length > 0) {
      uiCardStart('Предупреждения');
      warnings.forEach((warning) => uiCardLine(`${uiTone('◇', 'warning', true)} ${uiTone(warning, 'warning')}`));
      uiCardEnd();
    }

    const agentsPolicy = inspection.agentsExists ? await askAgentsPolicy(rl) : 'replace';

    const confirmed = await askYesNo(rl, 'Продолжить установку?', false);
    if (!confirmed) {
      printUiNotice('Установка отменена.', 'danger');
      process.exit(1);
    }

    ensureTargetDirectory(targetRepo, { assumeYes: true, interactive: true });

    await runInstallFlow({
      targetRepo,
      addArgs: DEFAULT_SKILLS_ADD_ARGS,
      install: true,
      bootstrap: true,
      syncMode: inspection.memoryBankExists,
      interactive: true,
      agentsPolicy,
    });
  } finally {
    rl.close();
  }
}

async function runInstallFlow({ targetRepo, addArgs, install, bootstrap, syncMode, interactive = false, agentsPolicy = 'keep' }) {
  let tempRoot;
  let preparedRepo;
  const restoreSourceLock = install && !bootstrap && resolve(targetRepo) === repoRoot;
  const sourceLockPath = join(repoRoot, SKILLS_LOCK_FILE);
  const sourceLockSnapshot = restoreSourceLock ? snapshotFile(sourceLockPath) : null;

  try {
    printInstallStage(1, 'Подготовка установщика...', interactive);
    ({ tempRoot, preparedRepo } = prepareRepository({ quiet: interactive }));
    if (interactive) printUiNotice('Пакет установки подготовлен.', 'success');

    if (install) {
      printInstallStage(2, 'Установка команд...', interactive);
      await installRuntimeCommandSkills(preparedRepo, targetRepo, addArgs, syncMode);
      if (sourceLockSnapshot) restoreFile(sourceLockPath, sourceLockSnapshot);
    } else {
      printInstallStage(2, 'Установка команд пропущена.', interactive);
    }

    if (bootstrap) {
      printInstallStage(3, syncMode ? 'Обновление Memory Bank...' : 'Создание Memory Bank...', interactive);
      await bootstrapTarget(preparedRepo, targetRepo, syncMode, agentsPolicy);
    } else {
      printInstallStage(3, 'Создание Memory Bank пропущено.', interactive);
    }

    if (interactive) {
      uiCardStart('Установка завершена');
      uiCardLine(`${uiTone('Путь', 'muted')}         ${uiTone(targetRepo, 'silver', true)}`);
      uiCardLine(`${uiTone('Команды', 'muted')}      ${uiTone(install ? 'установлены' : 'не менялись', 'success')}`);
      uiCardLine(`${uiTone('Memory Bank', 'muted')}  ${uiTone(bootstrap ? (syncMode ? 'обновлён' : 'создан') : 'не менялся', 'success')}`);
      if (bootstrap) {
        uiCardLine();
        uiCardLine(`${uiTone('Следующий шаг', 'muted')}  ${uiKeycap('/cold-start')}`);
      }
      uiCardEnd();
    } else {
      console.log('\nУстановка завершена.');
      console.log(`  Путь проекта: ${targetRepo}`);
      console.log(`  Команды: ${install ? 'установлены' : 'не менялись'}`);
      console.log(`  Memory Bank: ${bootstrap ? (syncMode ? 'обновлен' : 'создан') : 'не менялся'}`);
      if (bootstrap) console.log('  Следующий шаг: /cold-start');
    }
  } finally {
    if (tempRoot && preparedRepo) cleanupTemp(tempRoot, preparedRepo);
    if (interactive) {
      console.log('');
    }
  }
}

async function main() {
  if (args.length === 0) {
    await interactiveInstall();
    return;
  }

  const targetRepo = resolve(readArgValue('--target') || process.cwd());
  const bootstrapRequested = hasArg('--bootstrap') || hasArg('--bootstrap-only');
  const bootstrapOnly = hasArg('--bootstrap-only');
  const installOnly = hasArg('--install-only');
  const explicitNoTui = hasArg('--no-tui');
  const install = !bootstrapOnly;
  const bootstrap = bootstrapRequested && !installOnly;
  const addArgs = collectSkillsAddArgs({ defaultSkillAll: bootstrapRequested || explicitNoTui || installOnly });

  if (bootstrapOnly && installOnly) {
    console.error('Use only one of --bootstrap-only or --install-only.');
    process.exit(1);
  }

  if (explicitNoTui && !bootstrapRequested && !installOnly) {
    if (hasArg('--target')) {
      ensureTargetDirectory(targetRepo, { assumeYes: hasArg('--yes'), interactive: false });
    }
    await runInstallFlow({
      targetRepo,
      addArgs,
      install: true,
      bootstrap: false,
      syncMode: false,
    });
    return;
  }

  if (!bootstrapRequested) {
    if (hasArg('--target')) {
      ensureTargetDirectory(targetRepo, { assumeYes: hasArg('--yes'), interactive: false });
    }
    await runInstallFlow({
      targetRepo,
      addArgs,
      install: true,
      bootstrap: false,
      syncMode: false,
    });
    return;
  }

  ensureTargetDirectory(targetRepo, { assumeYes: hasArg('--yes'), interactive: false });
  const inspection = inspectTarget(targetRepo);
  printTargetInspection(targetRepo, inspection);

  if (!hasArg('--yes')) {
    console.error('\nNon-interactive bootstrap requires --yes after reviewing target warnings.');
    process.exit(1);
  }

  if (inspection.agentsExists) {
    console.log('Warning: AGENTS.md already exists. Generated AGENTS.md may be synced; custom AGENTS.md stays unchanged in non-interactive mode.');
  }
  if (inspection.memoryBankExists) {
    console.log('Warning: .memory-bank/ already exists; generated assets will be synced.');
  }
  if (bootstrapOnly && (hasArg('--sync') || inspection.memoryBankExists)) {
    console.log('Warning: --bootstrap-only sync repairs Memory Bank managed assets only; runtime command skills are not updated. Use --bootstrap --sync for a coherent framework upgrade.');
  }

  await runInstallFlow({
    targetRepo,
    addArgs,
    install,
    bootstrap,
    syncMode: hasArg('--sync') || inspection.memoryBankExists,
  });
}

