function canAnimateFileOutput() {
  return canUseColor()
    && !process.env.CI
    && process.env.DEVRAILS_NO_ANIMATION !== '1';
}

function fileGradientStops(line) {
  const operation = line.trimStart()[0];
  if (operation === '~') return FILE_GRADIENT_STOPS.updated;
  if (operation === '-') return FILE_GRADIENT_STOPS.removed;
  if (operation === '=') return FILE_GRADIENT_STOPS.unchanged;
  return FILE_GRADIENT_STOPS.created;
}

function colorizeFileLine(line, fadeLevel) {
  const chars = Array.from(line);
  const visibleLength = Math.max(1, chars.filter((char) => char !== ' ').length - 1);
  const gradientStops = fileGradientStops(line);
  let visibleIndex = 0;

  return chars.map((char) => {
    if (char === ' ') return char;

    const target = interpolateGradient(gradientStops, visibleIndex / visibleLength);
    const dimmedTarget = target.map((channel) => Math.round(channel * 0.12));
    const [red, green, blue] = interpolateColor(dimmedTarget, target, fadeLevel);
    visibleIndex += 1;
    return `\x1b[38;2;${red};${green};${blue}m${char}`;
  }).join('') + '\x1b[0m';
}

function wait(milliseconds) {
  return new Promise((resolveWait) => setTimeout(resolveWait, milliseconds));
}

async function printFileLine(line) {
  if (!canAnimateFileOutput()) {
    console.log(line);
    return;
  }

  for (const fadeLevel of FILE_FADE_LEVELS) {
    process.stdout.write(`\r\x1b[2K${colorizeFileLine(line, fadeLevel)}`);
    await wait(FILE_FADE_FRAME_MS);
  }
  process.stdout.write('\n');
}

async function printAnimatedFileLines(lines) {
  if (!canAnimateFileOutput()) {
    lines.forEach((line) => console.log(line));
    return;
  }

  process.stdout.write('\x1b[?25l');
  try {
    for (const line of lines) {
      await printFileLine(line);
    }
  } finally {
    process.stdout.write('\x1b[?25h');
  }
}

function isFileOperationLine(line) {
  return /^  [+=~-] /.test(line);
}

async function printAnimatedCommandOutput(output) {
  if (!output) return;
  if (!canAnimateFileOutput()) {
    process.stdout.write(output);
    return;
  }

  const lines = output.replace(/\r\n/g, '\n').split('\n');
  const endsWithNewline = output.endsWith('\n');
  process.stdout.write('\x1b[?25l');
  try {
    for (let index = 0; index < lines.length; index += 1) {
      if (index === lines.length - 1 && endsWithNewline && lines[index] === '') break;
      if (isFileOperationLine(lines[index])) {
        await printFileLine(lines[index]);
      } else {
        process.stdout.write(`${lines[index]}\n`);
      }
    }
  } finally {
    process.stdout.write('\x1b[?25h');
  }
}

async function runWithAnimatedFileOutput(command, commandArgs, options = {}) {
  if (!canAnimateFileOutput()) {
    run(command, commandArgs, options);
    return;
  }

  const result = spawnInstallerCommand(command, commandArgs, {
    encoding: 'utf8',
    stdio: ['inherit', 'pipe', 'inherit'],
    ...options,
  });

  if (result.error || result.status !== 0) {
    if (result.stdout) process.stdout.write(result.stdout);
    if (result.error) console.error(result.error.message);
    process.exit(result.status ?? 1);
  }

  await printAnimatedCommandOutput(result.stdout || '');
}

function prepareRepository({ quiet = false } = {}) {
  const tempRoot = mkdtempSync(join(tmpdir(), 'devrails26-skills-'));
  const preparedRepo = join(tempRoot, 'repo');

  cpSync(repoRoot, preparedRepo, {
    recursive: true,
    filter: (source) => {
      const rel = resolve(source).slice(repoRoot.length + 1);
      const root = rel.split(/[\\/]/)[0];
      return !PREPARE_EXCLUDED_ROOTS.has(root);
    },
  });

  run(process.execPath, ['scripts/vendor-shared.mjs'], {
    cwd: preparedRepo,
    env: { ...process.env, MEMOBANK_VENDOR_SHARED_ALLOW: '1' },
    ...(quiet ? { stdio: ['inherit', 'ignore', 'inherit'] } : {}),
  });

  return { tempRoot, preparedRepo };
}

function cleanupTemp(tempRoot, preparedRepo) {
  if (!process.env.DEVRAILS_KEEP_INSTALL_TMP && !process.env.MEMOBANK_KEEP_INSTALL_TMP) {
    rmSync(tempRoot, { recursive: true, force: true });
    return;
  }

  console.log(`Prepared repository kept at: ${preparedRepo}`);
}

function listMarkdownFiles(absDir) {
  return readdirSync(absDir, { withFileTypes: true })
    .filter((entry) => entry.isFile())
    .map((entry) => entry.name)
    .filter((name) => name.toLowerCase().endsWith('.md'))
    .sort((a, b) => a.localeCompare(b));
}

function extractFrontmatterDescription(markdown) {
  const match = markdown.match(/^---\r?\n([\s\S]*?)\r?\n---/);
  if (!match) return null;
  const desc = match[1].match(/^description:\s*(.+)$/m);
  if (!desc) return null;
  return desc[1].trim().replace(/^"(.*)"$/, '$1').replace(/^'(.*)'$/, '$1');
}

function yamlQuote(value) {
  const escaped = String(value).replace(/\\/g, '\\\\').replace(/"/g, '\\"');
  return `"${escaped}"`;
}

function hasRuntimeGeneratedMarker(content) {
  return content.includes(RUNTIME_SKILL_GENERATED_MARKER)
    || content.includes(LEGACY_INIT_GENERATED_MARKER)
    || content.includes(DEVRAILS_INIT_GENERATED_MARKER);
}

function normalizeText(content) {
  return content.replace(/\r\n/g, '\n').trim();
}

function isLegacyGeneratedProxySkill(content, expectedName = null) {
  const normalized = normalizeText(content);
  if (hasRuntimeGeneratedMarker(normalized)) return true;

  const proxy = normalized.match(/^---\n[\s\S]*?\n---\n\nRead and follow the instructions in `\.memory-bank\/commands\/([^`]+)\.md`$/);
  if (!proxy) return false;

  return expectedName === null || proxy[1] === expectedName;
}

function resolveCommandSpecs(preparedRepo) {
  const commandDir = join(preparedRepo, COMMAND_SPECS_REL);
  if (!existsSync(commandDir)) {
    console.error(`Missing command specs in prepared repository: ${commandDir}`);
    process.exit(1);
  }

  return listMarkdownFiles(commandDir)
    .map((filename) => {
      const name = filename.replace(/\.md$/i, '');
      const absPath = join(commandDir, filename);
      const content = readFileSync(absPath, 'utf8').replace(/\r\n/g, '\n');

      return {
        name,
        filename,
        content,
        desc: extractFrontmatterDescription(content) || `/${name}`,
      };
    })
    .filter(({ name }) => !LEGACY_ALIAS_COMMANDS.has(name));
}

function collectRequestedRuntimeSkills(addArgs) {
  const requested = [];
  const ignoredFlags = [];

  for (let i = 0; i < addArgs.length; i += 1) {
    const arg = addArgs[i];
    if (arg === '--skill') {
      const value = addArgs[i + 1];
      if (value && !value.startsWith('--')) {
        requested.push(value);
        i += 1;
      }
      continue;
    }
    if (arg.startsWith('--skill=')) {
      requested.push(arg.slice('--skill='.length));
      continue;
    }
    if (arg === '--yes') continue;
    if (arg === '--global') {
      ignoredFlags.push(arg);
      continue;
    }
    if (arg.startsWith('--')) ignoredFlags.push(arg);
  }

  return {
    all: requested.length === 0 || requested.includes('*'),
    names: new Set(requested.filter((name) => name && name !== '*')),
    ignoredFlags,
  };
}

function ensureRuntimeSkillDir(targetRepo, runtimeRoot, name) {
  const absDir = join(targetRepo, runtimeRoot, name);
  mkdirSync(absDir, { recursive: true });
  return absDir;
}

function runtimeSkillContent({ name, desc, content }) {
  return `---\nname: ${name}\ndescription: ${yamlQuote(desc)}\n---\n${RUNTIME_SKILL_GENERATED_MARKER}\n\n${content.trimEnd()}\n`;
}

function writeRuntimeSkill(targetRepo, runtimeRoot, spec) {
  const skillDir = join(targetRepo, runtimeRoot, spec.name);
  const skillPath = join(skillDir, 'SKILL.md');
  const relSkill = `${runtimeRoot}/${spec.name}/SKILL.md`;
  const content = runtimeSkillContent(spec);
  const existed = existsSync(skillDir);

  if (existed) {
    rmSync(skillDir, { recursive: true, force: true });
  }

  ensureRuntimeSkillDir(targetRepo, runtimeRoot, spec.name);
  writeFileSync(skillPath, content, 'utf8');
  return `  ${existed ? '~' : '+'} ${relSkill}`;
}

function cleanupObsoleteDevRailsRuntimeSkills(
  targetRepo,
  runtimeRoot,
  obsoleteNames = OBSOLETE_DEVRAILS_RUNTIME_SKILLS,
  { allowUnmarkedLegacyPackage = true } = {},
) {
  const absRoot = join(targetRepo, runtimeRoot);
  if (!existsSync(absRoot)) return;

  obsoleteNames.forEach((name) => {
    const skillDir = join(absRoot, name);
    const skillPath = join(skillDir, 'SKILL.md');
    if (!existsSync(skillPath)) return;

    const current = readFileSync(skillPath, 'utf8');
    const normalizedLines = new Set(
      current
        .replace(/\r\n/g, '\n')
        .split('\n')
        .map((line) => line.trim()),
    );
    const hasKnownLegacyHeading = [...normalizedLines].some(
      (line) => line === `# ${name}` || line.startsWith(`# ${name} `),
    );
    const isKnownLegacyPackage = normalizedLines.has(`name: ${name}`)
      && hasKnownLegacyHeading;
    const isGeneratedMatch = hasRuntimeGeneratedMarker(current)
      || (!allowUnmarkedLegacyPackage && isLegacyGeneratedProxySkill(current, name));
    if (!isGeneratedMatch && !(allowUnmarkedLegacyPackage && isKnownLegacyPackage)) {
      console.log(`  ! ${runtimeRoot}/${name} is obsolete but does not match the DevRails legacy skill; left unchanged`);
      return;
    }

    rmSync(skillDir, { recursive: true, force: true });
    console.log(`  - ${runtimeRoot}/${name}`);
  });
}

function cleanupRuntimeSkills(targetRepo, runtimeRoot, expectedNames, syncMode) {
  if (!syncMode) return;

  const absRoot = join(targetRepo, runtimeRoot);
  if (!existsSync(absRoot)) return;

  readdirSync(absRoot, { withFileTypes: true })
    .filter((entry) => entry.isDirectory())
    .forEach((entry) => {
      if (expectedNames.has(entry.name)) return;

      const relSkill = `${runtimeRoot}/${entry.name}`;
      const skillPath = join(absRoot, entry.name, 'SKILL.md');
      if (!existsSync(skillPath)) {
        console.log(`  ! ${relSkill} is obsolete but has no generated SKILL.md marker; left unchanged`);
        return;
      }

      const current = readFileSync(skillPath, 'utf8');
      if (!hasRuntimeGeneratedMarker(current) && !isLegacyGeneratedProxySkill(current, entry.name)) {
        console.log(`  ! ${relSkill} is obsolete but not marked as generated; left unchanged`);
        return;
      }

      rmSync(join(absRoot, entry.name), { recursive: true, force: true });
      console.log(`  - ${relSkill}`);
    });
}

function renamedRuntimeSkillsForSelection(expectedNames) {
  const renamedNames = new Set();
  RENAMED_RUNTIME_SKILLS.forEach((newName, oldName) => {
    if (expectedNames.has(newName)) renamedNames.add(oldName);
  });
  return renamedNames;
}

async function installRuntimeCommandSkills(preparedRepo, targetRepo, addArgs, syncMode) {
  const specs = resolveCommandSpecs(preparedRepo);
  const selection = collectRequestedRuntimeSkills(addArgs);

  selection.ignoredFlags.forEach((flag) => {
    console.log(`  ! ${flag} ignored: ${PRODUCT_NAME} installs project runtime skills into the target repository.`);
  });

  const selectedSpecs = selection.all
    ? specs
    : specs.filter(({ name }) => selection.names.has(name));

  if (selectedSpecs.length === 0) {
    console.log('  ! No matching runtime command skills selected.');
  }

  if (!selection.all) {
    const installedNames = new Set(selectedSpecs.map(({ name }) => name));
    selection.names.forEach((name) => {
      if (!installedNames.has(name)) {
        console.log(`  ! Unknown runtime command skill: ${name}`);
      }
    });
  }

  const expectedNames = new Set(selectedSpecs.map(({ name }) => name));
  if (selection.all) {
    cleanupObsoleteDevRailsRuntimeSkills(targetRepo, '.agents/skills');
    cleanupObsoleteDevRailsRuntimeSkills(targetRepo, '.claude/skills');
    cleanupObsoleteDevRailsRuntimeSkills(
      targetRepo,
      '.agents/skills',
      new Set(RENAMED_RUNTIME_SKILLS.keys()),
      { allowUnmarkedLegacyPackage: false },
    );
    cleanupObsoleteDevRailsRuntimeSkills(
      targetRepo,
      '.claude/skills',
      new Set(RENAMED_RUNTIME_SKILLS.keys()),
      { allowUnmarkedLegacyPackage: false },
    );
    cleanupRuntimeSkills(targetRepo, '.agents/skills', expectedNames, syncMode);
    cleanupRuntimeSkills(targetRepo, '.claude/skills', expectedNames, syncMode);
  } else {
    if (expectedNames.has('verify')) {
      cleanupObsoleteDevRailsRuntimeSkills(
        targetRepo,
        '.agents/skills',
        VERIFY_OBSOLETE_RUNTIME_SKILLS,
      );
      cleanupObsoleteDevRailsRuntimeSkills(
        targetRepo,
        '.claude/skills',
        VERIFY_OBSOLETE_RUNTIME_SKILLS,
      );
    }

    const renamedNames = renamedRuntimeSkillsForSelection(expectedNames);
    cleanupObsoleteDevRailsRuntimeSkills(
      targetRepo,
      '.agents/skills',
      renamedNames,
      { allowUnmarkedLegacyPackage: false },
    );
    cleanupObsoleteDevRailsRuntimeSkills(
      targetRepo,
      '.claude/skills',
      renamedNames,
      { allowUnmarkedLegacyPackage: false },
    );
  }

  for (const spec of selectedSpecs) {
    await printAnimatedFileLines([
      writeRuntimeSkill(targetRepo, '.agents/skills', spec),
      writeRuntimeSkill(targetRepo, '.claude/skills', spec),
    ]);
  }
}

function snapshotFile(filePath) {
  if (!existsSync(filePath)) return { existed: false, content: null };
  return { existed: true, content: readFileSync(filePath) };
}

function restoreFile(filePath, snapshot) {
  if (snapshot.existed) {
    writeFileSync(filePath, snapshot.content);
    return;
  }

  rmSync(filePath, { force: true });
}

async function bootstrapTarget(preparedRepo, targetRepo, syncMode, agentsPolicy = 'keep') {
  const initScript = join(preparedRepo, 'skills', 'mb-init', 'scripts', 'shared-init-mb.js');
  if (!existsSync(initScript)) {
    console.error(`Missing bootstrap script in prepared repository: ${initScript}`);
    process.exit(1);
  }

  const initArgs = syncMode ? [initScript, '--sync'] : [initScript];
  await runWithAnimatedFileOutput(process.execPath, initArgs, {
    cwd: targetRepo,
    env: {
      ...process.env,
      DEVRAILS_AGENTS_POLICY: agentsPolicy,
    },
  });
}

function isWritableDirectory(targetRepo) {
  try {
    accessSync(targetRepo, constants.W_OK);
    return true;
  } catch {
    return false;
  }
}

function findExistingParent(targetRepo) {
  let current = resolve(targetRepo);
  while (!existsSync(current)) {
    const parent = dirname(current);
    if (parent === current) return null;
    current = parent;
  }
  return current;
}

function isWritableTargetPath(targetRepo) {
  if (existsSync(targetRepo)) {
    try {
      return statSync(targetRepo).isDirectory() && isWritableDirectory(targetRepo);
    } catch {
      return false;
    }
  }

  const parent = findExistingParent(targetRepo);
  if (!parent) return false;

  try {
    return statSync(parent).isDirectory() && isWritableDirectory(parent);
  } catch {
    return false;
  }
}

function gitStatus(targetRepo) {
  if (!existsSync(targetRepo)) {
    return { isRepo: false, clean: null, summary: 'will be created' };
  }

  const inside = runCapture('git', ['-C', targetRepo, 'rev-parse', '--is-inside-work-tree']);
  if (inside.status !== 0 || inside.stdout.trim() !== 'true') {
    return { isRepo: false, clean: null, summary: 'not a git repository' };
  }

  const status = runCapture('git', ['-C', targetRepo, 'status', '--short']);
  if (status.status !== 0) {
    return { isRepo: true, clean: null, summary: 'git status unavailable' };
  }

  const output = status.stdout.trim();
  return {
    isRepo: true,
    clean: output.length === 0,
    summary: output.length === 0 ? 'git repository, clean' : 'git repository, has uncommitted changes',
  };
}

function inspectTarget(targetRepo) {
  const exists = existsSync(targetRepo);
  const isDirectory = exists ? statSync(targetRepo).isDirectory() : false;
  const memoryBankExists = isDirectory && existsSync(join(targetRepo, '.memory-bank'));
  const agentsExists = isDirectory && existsSync(join(targetRepo, 'AGENTS.md'));

  return {
    exists,
    isDirectory,
    memoryBankExists,
    agentsExists,
    git: gitStatus(targetRepo),
    writable: isWritableTargetPath(targetRepo),
  };
}

function ensureTargetDirectory(targetRepo, { assumeYes, interactive }) {
  if (!existsSync(targetRepo)) {
    if (!assumeYes && !interactive) {
      console.error(`Target does not exist: ${targetRepo}`);
      process.exit(1);
    }
    mkdirSync(targetRepo, { recursive: true });
    if (interactive) {
      printUiNotice(`Создана папка проекта: ${targetRepo}`, 'success');
    } else {
      console.log(`Created target directory: ${targetRepo}`);
    }
  }

  if (!statSync(targetRepo).isDirectory()) {
    console.error(`Target is not a directory: ${targetRepo}`);
    process.exit(1);
  }

  if (!isWritableDirectory(targetRepo)) {
    console.error(`Target is not writable: ${targetRepo}`);
    process.exit(1);
  }
}

async function askYesNo(rl, label, defaultYes = false) {
  const suffix = defaultYes ? '[Y/n]' : '[y/N]';
  const answer = (await rl.question(uiPrompt(`${label} ${suffix}`))).trim().toLowerCase();
  if (!answer) return defaultYes;
  if (['y', 'yes', 'д', 'да'].includes(answer)) return true;
  if (['n', 'no', 'н', 'нет'].includes(answer)) return false;
  return false;
}

