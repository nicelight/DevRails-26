function addGeneratedMarker(markdown) {
  if (markdown.includes(GENERATED_MARKER)) return markdown;

  const frontmatter = markdown.match(/^(---\r?\n[\s\S]*?\r?\n---\r?\n?)/);
  if (frontmatter) {
    return `${frontmatter[1]}${GENERATED_MARKER}\n${markdown.slice(frontmatter[1].length)}`;
  }

  return `${GENERATED_MARKER}\n${markdown}`;
}

function hasGeneratedMarker(content) {
  return content.includes(GENERATED_MARKER)
    || content.includes(LEGACY_GENERATED_MARKER)
    || content.includes(RUNTIME_SKILL_GENERATED_MARKER);
}

function uniqueExistingAgentsContent(existingContent, generatedContent) {
  const generatedLines = new Set(
    generatedContent
      .replace(/\r\n/g, '\n')
      .split('\n')
      .map((line) => line.trimEnd())
  );
  const uniqueLines = [];
  let previousBlank = true;

  existingContent
    .replace(/\r\n/g, '\n')
    .split('\n')
    .forEach((line) => {
      const normalized = line.trimEnd();
      if (normalized === '') {
        if (!previousBlank && uniqueLines.length) {
          uniqueLines.push('');
          previousBlank = true;
        }
        return;
      }
      if (generatedLines.has(normalized)) return;

      uniqueLines.push(normalized);
      previousBlank = false;
    });

  while (uniqueLines[uniqueLines.length - 1] === '') uniqueLines.pop();
  return uniqueLines.join('\n');
}

function mergeAgentsGuide(existingContent, generatedContent) {
  const uniqueExistingContent = uniqueExistingAgentsContent(existingContent, generatedContent);
  if (!uniqueExistingContent.trim()) return `${generatedContent.trimEnd()}\n`;

  return `${generatedContent.trimEnd()}

---

## Preserved Previous AGENTS.md

The following content existed before DevRails 26 bootstrap and was appended automatically.
Review it for contradictions with the generated guide above.

${uniqueExistingContent}
`;
}

function isGeneratedAgentsGuide(content) {
  const normalized = content.replace(/\r\n/g, '\n');
  return hasGeneratedMarker(normalized) || (
    normalized.startsWith('# Agent Operating Guide (Project Map)\n') &&
    normalized.includes('## Runtime vs durable memory') &&
    normalized.includes('## Entry points')
  );
}

function symlinkOrCopy(target, linkName) {
  const linkPath = path.join(ROOT, linkName);
  if (fs.existsSync(linkPath)) return;

  try {
    fs.symlinkSync(target, linkPath);
    console.log(`  ~ ${linkName} -> ${target}`);
  } catch (e) {
    // Windows / restricted env fallback
    const targetPath = path.join(ROOT, target);
    if (fs.existsSync(targetPath)) {
      fs.writeFileSync(linkPath, fs.readFileSync(targetPath, 'utf8'));
      console.log(`  + ${linkName} (copy, symlink failed)`);
    } else {
      console.log(`  ! Could not create ${linkName}: missing target ${target}`);
    }
  }
}

