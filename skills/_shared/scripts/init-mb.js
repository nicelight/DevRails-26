function ensureDir(rel) {
  const p = path.join(ROOT, rel);
  if (!fs.existsSync(p)) {
    fs.mkdirSync(p, { recursive: true });
    console.log(`  + ${rel}/`);
  }
}

function writeFile(rel, content, { overwrite = false } = {}) {
  const p = path.join(ROOT, rel);
  const existed = fs.existsSync(p);
  if (existed && !overwrite) {
    if (rel === 'AGENTS.md') {
      const current = fs.readFileSync(p, 'utf8');
      if (AGENTS_POLICY === 'replace') {
        overwrite = true;
      } else if (AGENTS_POLICY === 'merge') {
        content = mergeAgentsGuide(current, content);
        overwrite = true;
        agentsMerged = true;
      } else if (SYNC_MODE && isGeneratedAgentsGuide(current)) {
        overwrite = true;
      } else {
        console.log(`  ! ${rel} exists but is not recognized as generated; left unchanged`);
        return;
      }
    } else {
      return;
    }
  }

  fs.writeFileSync(p, content, 'utf8');
  console.log(`  ${existed ? '~' : '+'} ${rel}${existed ? ' (updated)' : ''}`);
}

function readUtf8(absPath) {
  return fs.readFileSync(absPath, 'utf8');
}

function resolveReferenceFile(category, filename) {
  const nestedPath = path.join(REFERENCES_DIR, category, filename);
  if (fs.existsSync(nestedPath)) return nestedPath;

  const flattenedPath = path.join(REFERENCES_DIR, `shared-${category}-${filename}`);
  if (fs.existsSync(flattenedPath)) return flattenedPath;

  return null;
}

function resolveTopLevelReferenceFile(filename) {
  const directPath = path.join(REFERENCES_DIR, filename);
  if (fs.existsSync(directPath)) return directPath;

  const flattenedPath = path.join(REFERENCES_DIR, `shared-${filename}`);
  if (fs.existsSync(flattenedPath)) return flattenedPath;

  return null;
}

