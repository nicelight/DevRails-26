function extractFrontmatterDescription(markdown) {
  const fm = markdown.match(/^---\r?\n([\s\S]*?)\r?\n---\r?\n/);
  if (!fm) return null;
  const m = fm[1].match(/^description:\s*(.*)\s*$/m);
  if (!m) return null;
  const value = m[1].trim();
  if (!value) return null;
  return value.replace(/^"(.*)"$/, '$1').replace(/^'(.*)'$/, '$1');
}

function yamlQuote(value) {
  const escaped = String(value).replace(/\\/g, '\\\\').replace(/"/g, '\\"');
  return `"${escaped}"`;
}

function addGeneratedMarker(markdown) {
  if (markdown.includes(GENERATED_MARKER)) return markdown;

  const frontmatter = markdown.match(/^(---\r?\n[\s\S]*?\r?\n---\r?\n?)/);
  if (frontmatter) {
    return `${frontmatter[1]}${GENERATED_MARKER}\n${markdown.slice(frontmatter[1].length)}`;
  }

  return `${GENERATED_MARKER}\n${markdown}`;
}

function hasGeneratedMarker(content) {
  return content.includes(GENERATED_MARKER);
}

function isGeneratedAgentsGuide(content) {
  const normalized = content.replace(/\r\n/g, '\n');
  return hasGeneratedMarker(normalized) || (
    normalized.startsWith('# Agent Operating Guide (Project Map)\n') &&
    normalized.includes('## Runtime vs durable memory') &&
    normalized.includes('## Entry points')
  );
}

function isGeneratedProxySkill(content, expectedName = null) {
  const normalized = content.replace(/\r\n/g, '\n').trim();
  if (hasGeneratedMarker(normalized)) return true;

  const proxy = normalized.match(/^---\n[\s\S]*?\n---\n\nRead and follow the instructions in `\.memory-bank\/commands\/([^`]+)\.md`$/);
  if (!proxy) return false;

  return expectedName === null || proxy[1] === expectedName;
}

function escapeRegExp(value) {
  return String(value).replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function isLegacyAliasCommandSpec(filename, content) {
  const name = filename.replace(/\.md$/i, '');
  if (!LEGACY_ALIAS_COMMANDS.has(name)) return false;

  const normalized = content.replace(/\r\n/g, '\n');
  const aliasHeading = new RegExp(`^#\\s+/${escapeRegExp(name)}\\s+[—-]\\s+Alias\\b`, 'm');
  return aliasHeading.test(normalized) || /^description:\s*.*(Alias|Алиас)/im.test(normalized);
}

function removePath(rel) {
  fs.rmSync(path.join(ROOT, rel), { recursive: true, force: true });
  console.log(`  - ${rel}`);
}

function cleanupGeneratedCommandSpecs(expectedFilenames) {
  if (!SYNC_MODE) return;

  const relDir = `${MB}/commands`;
  const absDir = path.join(ROOT, relDir);
  if (!fs.existsSync(absDir)) return;

  listMarkdownFiles(absDir).forEach((filename) => {
    if (expectedFilenames.has(filename)) return;

    const rel = `${relDir}/${filename}`;
    const content = readUtf8(path.join(absDir, filename));
    if (hasGeneratedMarker(content)) {
      removePath(rel);
      return;
    }

    const legacyNote = isLegacyAliasCommandSpec(filename, content) ? ' legacy alias' : '';
    console.log(`  ! ${rel} is obsolete${legacyNote} but not marked as generated; left unchanged`);
  });
}

function cleanupGeneratedProxySkills(runtimeRoot, expectedNames) {
  if (!SYNC_MODE) return;

  const absRoot = path.join(ROOT, runtimeRoot);
  if (!fs.existsSync(absRoot)) return;

  fs.readdirSync(absRoot, { withFileTypes: true })
    .filter((entry) => entry.isDirectory())
    .forEach((entry) => {
      if (expectedNames.has(entry.name)) return;

      const relSkill = `${runtimeRoot}/${entry.name}`;
      const relSkillFile = `${relSkill}/SKILL.md`;
      const absSkillFile = path.join(ROOT, relSkillFile);
      if (!fs.existsSync(absSkillFile)) {
        console.log(`  ! ${relSkill} is obsolete but has no generated SKILL.md marker; left unchanged`);
        return;
      }

      const skillContent = readUtf8(absSkillFile);
      if (hasGeneratedMarker(skillContent)) {
        removePath(relSkill);
        return;
      }

      const proxyNote = isGeneratedProxySkill(skillContent, entry.name) ? ' generated-looking proxy' : '';
      console.log(`  ! ${relSkill} is obsolete${proxyNote} but not marked as generated; left unchanged`);
    });
}

function generateCommandsIndex(commandMetas) {
  const lines = [];
  lines.push('---');
  lines.push('description: Роутер по command-spec файлам (slash commands).');
  lines.push('status: active');
  lines.push('---');
  lines.push('# Commands');
  lines.push('');
  commandMetas.forEach(({ name, desc }) => {
    lines.push(`- [.memory-bank/commands/${name}.md](${name}.md): ${desc}`);
  });
  lines.push('');
  return lines.join('\n');
}

function seedCommandsFromTemplates() {
  const templates = resolveCommandTemplates()
    .filter(({ name }) => !LEGACY_ALIAS_COMMANDS.has(name));
  if (templates.length === 0) {
    console.error(`\nERROR: Command templates not found in: ${COMMAND_TEMPLATES_DIR} or flattened references.`);
    console.error('Run init-mb.js from the memobank_BMAD_SDD package (do not copy it standalone).');
    process.exit(1);
  }

  const expectedFilenames = new Set(templates.map(({ filename }) => filename));
  expectedFilenames.add('constitution.md');
  expectedFilenames.add('index.md');
  cleanupGeneratedCommandSpecs(expectedFilenames);

  const metas = [];

  templates.forEach(({ filename, absPath, name }) => {
    const content = readUtf8(absPath);
    const desc = extractFrontmatterDescription(content) || `/${name}`;

    writeFile(`${MB}/commands/${filename}`, addGeneratedMarker(content), { overwrite: SYNC_MODE });
    metas.push({ name, desc });
  });

  if (!metas.some(({ name }) => name === 'constitution')) {
    writeFile(`${MB}/commands/constitution.md`, addGeneratedMarker(constitutionCommandTemplate()), { overwrite: SYNC_MODE });
    metas.push({ name: 'constitution', desc: 'Read or minimally amend the Project Constitution.' });
    metas.sort((a, b) => a.name.localeCompare(b.name));
  }

  writeFile(`${MB}/commands/index.md`, addGeneratedMarker(generateCommandsIndex(metas)), { overwrite: SYNC_MODE });
  return metas;
}

